#################### Read in shapefile ###############################################################
shapefile.dir <- file.path(j, "FILEPATH")
shapefile.version <- "GBD2019_analysis_final"
shape <- readOGR(dsn = shapefile.dir, layer = shapefile.version)

################################################################################################
### Read in mean annual temperature (1980-2018)
################################################################################################

LOCATION_ID<-loc_id

for (i in 1:length(LOCATION_ID)){
  BRICK<-brick(paste0(j,"FILEPATH/annual_mean.grd"))
  crs(BRICK)<-"+proj=longlat +datum=WGS84 +no_defs +ellps=WGS84 +towgs84=0,0,0"

  sub_shape<-shape[shape@data$loc_id == LOCATION_ID[i],]

  temp_crop<-crop(BRICK,sub_shape)
  temp_sub<-mask(BRICK,sub_shape)

  test<-mean(temp_sub@data@values,na.rm=TRUE)

  if(is.na(test)==TRUE){
    temp_sub<-temp_crop
  }


  data <- as.data.table(rasterToPoints(temp_sub, spatial = TRUE))
  colnames(data)<-c("meanTempCat","x","y")
  data[,loc_id:=LOCATION_ID[i]]

  str(data)
  write_feather(data,paste0(j,"FILEPATH",LOCATION_ID[i],".feather"))
  print(i)
}


